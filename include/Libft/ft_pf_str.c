#include "ft_printf.h"

int	ft_pf_str(char *str)
{
	int	i;

	i = 0;
	if (str == 0)
	{
		write (2, "(null)", 6);
		return (6);
	}
	while (*str != '\0')
	{
		write (2, str, 1);
		str++;
		i++;
	}
	return (i);
}
